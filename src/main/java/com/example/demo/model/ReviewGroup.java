package com.example.demo.model;

public class ReviewGroup {

	private Long  id;
	private  String groupName;
	


	public ReviewGroup() {
		
	}
	public Long  getId() {
		return id;
	}
	public void setId(Long  id) {
		this.id = id;
	}
	public String getGroupName() {
		return groupName;
	}
	public void setGroupName(String groupName) {
		this.groupName = groupName;
	}
	
	
	

}
